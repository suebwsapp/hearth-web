    da: "da_DK",
    fi: "fi_FI",
    no: "nb_NO",
    sv: "sv_SE",
    pl: "pl_PL",
    ro: "ro_RO",
    hu: "hu_HU",
    el: "el_GR",
    cs: "cs_CZ",
    sk: "sk_SK",
